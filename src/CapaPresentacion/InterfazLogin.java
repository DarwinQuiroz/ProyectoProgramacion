    public void iniciarCarga() 
    { //Utilizando Hilos
        new Thread() 
        {
            @Override
            public void run() 
            {
                int i = 0;
                while (i <= 100) 
                {
                    i+=2;
                    Progreso.setValue(i);
                    try 
                    {
                        sleep(100);
                    } 
                    catch (Exception e) 
                    {
                        e.printStackTrace();
                    }
                }

            }
        }.start();
    }

